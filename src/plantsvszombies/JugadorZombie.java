    private void bAgregar5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregar5ActionPerformed
        String nombre = etNombre.getText();
        String cantidad = etCantidad.getText();

        if (validarNumero(cantidad) && Integer.parseInt(cantidad)>0) {

            if (nombre.equals("") || cantidad.equals("")) {
                JOptionPane.showMessageDialog(null, "Llene lo campos de nombre y cantidad");
            } else {
                if (aJugadorZombie.size() > 0) {
                    aJugadorZombie.set(0, nombre);
                    aJugadorZombie.set(1, cantidad);
                } else {
                    aJugadorZombie.add(0, nombre);
                    aJugadorZombie.add(1, cantidad);
                }
                contJugadorZombie = 1;
                cantidadZombies = Integer.parseInt(cantidad);

                String nuevoCampo = JOptionPane.showInputDialog("Inserte texto: ");
                if (nuevoCampo != null) {
                    aJugadorZombie.add(nuevoCampo);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese una cantidad v√°lida");
        }
    }//GEN-LAST:event_bAgregar5ActionPerformed

    private static boolean validarNumero(String cadena) {
        try {
            Integer.parseInt(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

